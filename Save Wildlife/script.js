//audio will not autoplay
audio.autoplay = false;

//play kangaroo noise on click when paused, if playing then pause
function playKangaroo() {
var kangaroo = document.getElementById("kangarooAudio");
    if (kangaroo.paused) {
        kangaroo.play();
    } else {
        kangaroo.pause();
    }
}

